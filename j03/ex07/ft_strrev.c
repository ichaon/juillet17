char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	char	rev;

	i = 0;
	n = 0;
	rev = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i = i - 1;
	while (i > n)
	{
		rev = str[i];
		str[i] = str[n];
		str[n] = rev;
		n++;
		i--;
	}
	return (str);
}
