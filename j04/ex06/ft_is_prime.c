int		ft_is_prime(int nb)
{
	int		i;
	int		res;

	i = 2;
	res = 1;
	if (nb <= 1)
		res = 0;
	while (i < nb && res != 0)
	{
		if ((nb % i) == 0)
			res = 0;
		i++;
	}
	return (res);
}
