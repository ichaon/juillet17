int		ft_str_is_lowercase(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (str[i] == '\0')
		return (1);
	else
	{
		while (str[i] != '\0')
			i++;
		while ('a' <= str[j] && str[j] <= 'z')
			j++;
		if (i == j)
			return (1);
		else
			return (0);
	}
}
